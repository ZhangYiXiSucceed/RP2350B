void OLED_WR_Byte(u8 data,u8 cmd)
{	
    u8 i;			  
    if(cmd)
      OLED_DC_Set();
    else 
      OLED_DC_Clr();		  
    OLED_CS_Clr();
    for(i=0;i<8;i++)
    {			  
      OLED_SCLK_Clr();
      if(data&0x80)
         OLED_SDIN_Set();
      else 
         OLED_SDIN_Clr();
      OLED_SCLK_Set();
      data<<=1;   
    }				 		  
    OLED_CS_Set();
    OLED_DC_Set();   	
} 

